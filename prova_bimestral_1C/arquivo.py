n1 =int( input("Digite um número para saber o seu sucessor: ")) #o (n1) vai ser a variavel que o usuário vai definir
n2= 1 #o (n2) vai ser a variavel fixa (1)
su= n1+n2 #a variavel (su) vai somar a variavel fixa (n2) mais a variavel definida pelo usuario (n1)
print("o sucessor do seu número é {}".format(su)) #aqui vai aparecer o sucessor que nada mais é do que a soma do n1 e n2.
